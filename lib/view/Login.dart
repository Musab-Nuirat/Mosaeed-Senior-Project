import 'package:flutter/material.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'HomePage.dart';
import 'SignUp.dart'; // to import the RoundedTextField
import '../model/localDatabase/sharedPrefferences.dart';



class LoginPage extends StatelessWidget {

  final TextEditingController emailController = TextEditingController();

  final TextEditingController passwordController = TextEditingController();

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text(
          'تسجيل الدخول',
          style: TextStyle(color: Color(0xFF92705B)),
        ),
      ),
      body: Container(
        alignment: Alignment.center,
        child: ListView(
          children: [
            Column(
              children: [
                const Image(
                  image: AssetImage("assets/logo.png"),
                  height: 200,
                ),
                SizedBox(
                  height: 25,
                ),
                RoundedTextField(
                  label: 'الايميل',
                  controller: emailController,
                ),
                const SizedBox(height: 25),
                RoundedTextField(
                  label: 'الرمز',
                  isPassword: true,
                  controller: passwordController,
                ),
                const SizedBox(height: 25),
                ElevatedButton(
                  onPressed: () {
                    loginUser(context);
                  },
                  style: ElevatedButton.styleFrom(
                    fixedSize: const Size(250, 60),
                    backgroundColor: const Color(0xFF842700),
                    foregroundColor: Colors.white,
                  ),
                  child: const Text('تسجيل الدخول'),
                ),
                const SizedBox(height: 16),
                Row(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    GestureDetector(
                      onTap: () {
                        Navigator.push(
                          context,
                          MaterialPageRoute(
                            builder: (context) => SignUpPage(),
                          ),
                        );
                      },
                      child: const Text(
                        'سجل الان',
                        style: TextStyle(
                            color: Color(0xFF92705B),
                            fontWeight: FontWeight.bold),
                      ),
                    ),
                    const Text(
                      'هل لديك حساب ؟ ',
                      style: TextStyle(color: Color(0xFF92705B)),
                    ),
                  ],
                ),
              ],
            )
          ],
        ),
      ),
    );
  }

  void loginUser(BuildContext context) async {
    String email = emailController.text;
    String password = passwordController.text;

    try {
      UserCredential userCredential =
          await FirebaseAuth.instance.signInWithEmailAndPassword(
        email: email,
        password: password,
      );

      // Get the Student ID generated by Firebase Authentication
      String studentid = userCredential.user?.uid ?? "";
      saveUserID(studentid);
      // User logged in successfully
      print("Login Successful");

      // navigation logic to the next screen after successful login
      Navigator.pushReplacement(
        context,
        MaterialPageRoute(
          builder: (context) => HomePage(studentId: studentid),
        ),
      );
    } catch (e) {
      print("Login Failed: $e");
      // Handle login failure
      // You can show an error message to the user
      ScaffoldMessenger.of(context).showSnackBar(SnackBar(
        content: Text("فشل التسجيل الرجاء التأكد من البيانات المدخلة والمحاولة مرة أخرى"),
        duration: Duration(seconds: 3),
      ));
    }
  }
}
